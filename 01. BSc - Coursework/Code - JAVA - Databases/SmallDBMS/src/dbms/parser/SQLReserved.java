package dbms.parser;

/**
 * This enumeration type contains all the reserved words that the user of our
 * DBMS system can use to make his SQl queries.
 *  
 */
public enum SQLReserved {
	AND, BY, CHAR, CREATE, DELETE, DROP, FROM, INDEX, INSERT, INTEGER, INTERSECTION, INTO, KEY, MINUS, ON, OR, ORDER, PRIMARY, SELECT, TABLE, UNION, VALUES, WHERE;
}
